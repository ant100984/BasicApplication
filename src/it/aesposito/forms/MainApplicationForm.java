    public void showDialog(final JDialog dialog, final Component c){
        dialog.setResizable(false);
        dialog.setLocation(DIALOG_POSITION);
        dialog.setDefaultCloseOperation(DO_NOTHING_ON_CLOSE);
        dialog.addWindowListener(new WindowAdapter() {
            
            @Override
            public void windowClosing(WindowEvent e){
                switch(((MySpecialForm)c).form_mode){
                    case INSERT_MODE:
                    case MODIFY_MODE:

                        if(JOptionPane.showConfirmDialog(c, "Sei sicuro di voler uscire? I dati non salvati andranno persi.","Attenzione",JOptionPane.OK_CANCEL_OPTION) == JOptionPane.OK_OPTION){
                            dialog.dispose();
                        }

                        break;
                    default:
                        dialog.dispose();
                }
                
            }
        });
        
        dialog.getContentPane().add(c);
        dialog.pack();
        dialog.setVisible(true);
    }

