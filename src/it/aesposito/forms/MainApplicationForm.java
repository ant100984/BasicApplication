    public static void main(String args[]) {
        try {
            
            String theme_class = "";
            
            SqlSession session = null;
            try {
                session = SQLMapClientFactory.getSqlMapClient().openSession();
            } catch (IOException ex) {
                Logger.getLogger(PreferenzeForm.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            ApplicationPropertiesMapper apm = session.getMapper(ApplicationPropertiesMapper.class);
            ApplicationPropertiesExample ape = new ApplicationPropertiesExample();
            List<ApplicationProperties> list_props = apm.selectByExample(ape);

            if(list_props == null || list_props.size() != 1)
                theme_class = Themes.getDefaultThemeClass();
            else
                theme_class = (String) Themes.getThemes().get(list_props.get(0).getTheme());
            
            Properties props = new Properties();
            props.put("logoString", "");
            
            Class c = Class.forName(theme_class);
            Method metodo = c.getMethod("setCurrentTheme", new Class[] {Properties.class});
            metodo.invoke(null, props);
            
            UIManager.setLookAndFeel(theme_class);
            
        } catch(Exception ex){
            try{
                UIManager.setLookAndFeel(Themes.getDefaultThemeClass());
            }catch(Exception sub_ex){
            }
        }
        //</editor-fold>

        /* Create and display the form */
        java.awt.EventQueue.invokeLater(new Runnable() {
            public void run() {
                MainApplicationForm maf = new MainApplicationForm();
                maf.setVisible(true);
                maf.setExtendedState(maf.getExtendedState() | JFrame.MAXIMIZED_BOTH);
            }
        });
    }
    
